class GenealogyDataTransformer:
    """Utility class for transforming genealogical data structures."""

    @staticmethod
    def map_person_data(person, string_mapper=None, date_mapper=None):
        """Transform person data with optional string and date mappers."""
        pass

    @staticmethod
    def map_family_data(family, string_mapper=None, date_mapper=None):
        """Transform family data with optional string and date mappers."""
        pass

    @staticmethod
    def map_events(events, person_mapper=None, string_mapper=None):
        """Transform personal or family events."""
        pass

    @staticmethod
    def generate_person_names(person):
        """Generate all possible name variations for a person."""
        pass
